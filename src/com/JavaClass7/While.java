package com.JavaClass7;

public class While {

	public static void main(String[] args) {

		int a=0;
		while (a>10 ) {
			System.out.println("Good morning");
			a++;
		
		int b=0;
		while(b<5) {
			System.out.println("I am inside while loop");
			b++;
		}
		}
		System.out.println("I am outside of while loop");
		
	}

}


