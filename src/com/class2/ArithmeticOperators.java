package com.class2;

public class ArithmeticOperators {

	public static void main(String[] args) {
		// declare 2 int variables and then we perform
		// addition, subtraction, multiplication, division
		int a = 30;
		int b = 15;
		System.out.print(a + b); // 45
		System.out.print(a - b); // 15
		System.out.print(a * b); // 450
		System.out.print(a / b); // 2

		int sum = a + b;
		int sub = a - b;
		int mult = a * b;
		int div = a / b;
		System.out.print(sum);
		System.out.print(sub);
		System.out.print(mult);
		System.out.print(div);
		// print: the__ of 2 numbers is __
		System.out.print("the addition of the two numbers is " + sum);
		System.out.print("the subtration of the two numbers is " + sub);
		System.out.print("the multiplication of the the two numbers is " + mult);
		System.out.print("the division of the two numbers is " + div);


	}

}
